package oops;

public class TypecastingIm {
	public static void main(String[] args) {
		int a=10;
		double d=a;
		byte b=5;
		long l=b;
		
		byte s1=10;
		float f=s1;
		
		short e=1234;
		long p=e;
		
		
		System.out.println(d);
		System.out.println(l+10);
		System.out.println(b);
		System.out.println(f);
		System.out.println(p);


	}
}
